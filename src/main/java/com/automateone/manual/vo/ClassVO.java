package com.automateone.manual.vo;

public class ClassVO {
	
	private String class_id;
	private String class_name;
	private String class_parent_id;
	private String class_version_id;
	private String reg_date;
	
	public String getClass_id() {
		return class_id;
	}
	public void setClass_id(String class_id) {
		this.class_id = class_id;
	}
	public String getClass_name() {
		return class_name;
	}
	public void setClass_name(String class_name) {
		this.class_name = class_name;
	}
	public String getClass_parent_id() {
		return class_parent_id;
	}
	public void setClass_parent_id(String class_parent_id) {
		this.class_parent_id = class_parent_id;
	}
	public String getClass_version_id() {
		return class_version_id;
	}
	public void setClass_version_id(String class_version_id) {
		this.class_version_id = class_version_id;
	}
	public String getReg_date() {
		return reg_date;
	}
	public void setReg_date(String reg_date) {
		this.reg_date = reg_date;
	}
	
	
}
