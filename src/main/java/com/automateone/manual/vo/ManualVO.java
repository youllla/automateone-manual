package com.automateone.manual.vo;

public class ManualVO {
	
	private String manual_id;
	private String category;
	private String version_id;
	private String title;
	private String content;
	private String parse_content;
	private String reg_date;
	private String upd_date;
	
	public String getManual_id() {
		return manual_id;
	}
	public void setManual_id(String manual_id) {
		this.manual_id = manual_id;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getVersionId() {
		return version_id;
	}
	public void setVersionId(String version_id) {
		this.version_id = version_id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getParse_content() {
		return parse_content;
	}
	public void setParse_content(String parse_content) {
		this.parse_content = parse_content;
	}
	public String getReg_date() {
		return reg_date;
	}
	public void setReg_date(String reg_date) {
		this.reg_date = reg_date;
	}
	public String getUpd_date() {
		return upd_date;
	}
	public void setUpd_date(String upd_date) {
		this.upd_date = upd_date;
	}

	
}
